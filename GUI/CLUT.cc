bool
CLUT::HasSaturation(Type type)
{
    if (type < kBlueSaturated || type >= kNumTypes) { type = kBlueSaturated; }
    return cluts_[type].hasSaturation_;
}

void
CLUT::AddTypeNames(QComboBox* widget)
{
    int index = widget->currentIndex();
    widget->clear();
    for (int index = 0; index < kNumTypes; ++index) { widget->addItem(cluts_[index].name_); }
    widget->setCurrentIndex(index);
}

CLUT::CLUT(Type type, GLfloat alpha) : colors_(0), type_(type), alpha_(alpha)
{
    Logger::ProcLog log("CLUT", Log());
    setType(type);
}

void
CLUT::setType(Type type)
{
    type_ = type;
    if (!cluts_[type].clut_) {
        switch (type) {
        case kRGB655: makeEncodedClut(cluts_[type], 6, 5, 5); break;
        case kRGB565: makeEncodedClut(cluts_[type], 5, 6, 5); break;
        case kRGB556: makeEncodedClut(cluts_[type], 5, 5, 6); break;
        default: abort();
        }
    }
    colors_ = cluts_[type].clut_;
}

Color
CLUT::getColor(double value) const
{
    static Logger::ProcLog log("getColor", Log());
    size_t index = size_t(::rint(value * (getSize() - 1)));
    LOGDEBUG << "value: " << value << " index: " << index << std::endl;
    return Color(colors_[index], alpha_);
}

bool
CLUT::hasSaturation() const
{
    return cluts_[type_].hasSaturation_;
}

void
CLUT::makeColorMapImage(QImage& image, bool showTicks)
{
    Logger::ProcLog log("makeColorMapImage", Log());

    const int width = image.width();
    const int height = image.height();
    const double scale = 1.0 / (width - 1);

    QPainter painter(&image);
    for (int index = 0; index < width; ++index) {
        double t = index * scale;
        const RGBColor& rgb(getColor(t));

        uint16_t r = ::rint(rgb.red * 255);
        uint16_t g = ::rint(rgb.green * 255);
        uint16_t b = ::rint(rgb.blue * 255);

        LOGDEBUG << "t: " << t << " RGB: " << rgb.red << ' ' << rgb.green << ' ' << rgb.blue << ' ' << std::hex << r
                 << ' ' << g << ' ' << b << std::dec << std::endl;

        painter.setPen(QColor(r, g, b));
        painter.drawLine(index, 0, index, height);
    }

    if (showTicks) {
        // Draw three tiny tick marks, at 25%, 50% and 75% span positions.
        //
        const int tickSpacing = width / 4;
        painter.setPen(Qt::black);
        for (int index = 1; index < 4; ++index) {
            const int x = index * tickSpacing;
            painter.drawLine(x, height - 3, x, height);
        }
    }
}

void
CLUT::makeGradiantImage(QImage& image, const RGBColor& on, bool showTicks)
{
    const int width = image.width();
    const int height = image.height();
    const double scale = 1.0 / (width - 1);

    QPainter painter(&image);
    for (int index = 0; index < width; ++index) {
        double t = index * scale;
        QColor color(int(::rint(on.red * t)), int(::rint(on.green * t)), int(::rint(on.blue * t)));
        painter.setPen(color);
        painter.drawLine(index, 0, index, height);
    }

    if (showTicks) {
        // Draw three tiny tick marks, at 25%, 50% and 75% span positions.
        //
        const int tickSpacing = width / 4;
        painter.setPen(Qt::black);
        for (int index = 1; index < 4; ++index) {
            const int x = index * tickSpacing;
            painter.drawLine(x, height - 3, x, height);
        }
    }
}
