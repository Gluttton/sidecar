static void
fillComponentValues(std::vector<GLfloat>& v, uint16_t bits)
{
    uint32_t size = 1 << bits;
    GLfloat scale = 1.0 / (size - 1);
    for (uint32_t index = 0; index < size; ++index) { v.push_back(scale * index); }
}

static void
makeEncodedClut(CLUTInfo& info, uint16_t redBitCount, uint16_t greenBitCount, uint16_t blueBitCount)
{
    Logger::ProcLog log("makeEncodedClut", CLUT::Log());

    // Generate RED values from 0.0 to 1.0 with the given bit resolution.
    //
    std::vector<GLfloat> redValues;
    fillComponentValues(redValues, redBitCount);

    // Generate GREEN values from 0.0 to 1.0 with the given bit resolution. If same resolution as RED, just copy
    // its values.
    //
    std::vector<GLfloat> greenValues;
    if (greenBitCount == redBitCount) {
        greenValues = redValues;
    } else {
        fillComponentValues(greenValues, greenBitCount);
    }

    // Generate BLUE values from 0.0 to 1.0 with the given bit resolution. If same resolution as RED or GREEN,
    // just copy their values.
    //
    std::vector<GLfloat> blueValues;
    if (blueBitCount == redBitCount) {
        blueValues = redValues;
    } else if (blueBitCount == greenBitCount) {
        blueValues = greenValues;
    } else {
        fillComponentValues(blueValues, blueBitCount);
    }

    // Create masks for extracting the color component bits from an index after any shifting.
    //
    const uint32_t redMask = (1 << redBitCount) - 1;
    const uint32_t greenMask = (1 << greenBitCount) - 1;
    const uint32_t blueMask = (1 << blueBitCount) - 1;

    LOGDEBUG << "RGB counts: " << redBitCount << ' ' << greenBitCount << ' ' << blueBitCount << std::endl;
    LOGDEBUG << "RGB masks: " << std::hex << redMask << ' ' << greenMask << ' ' << blueMask << std::dec << std::endl;

    // Allocate an array for our new component values
    //
    static const size_t kEncodedSize = 65536;
    RGBColor* p = new RGBColor[kEncodedSize];
    info.clut_ = p;
    info.size_ = kEncodedSize;

    // Fill the array wth our precalculated values generated above.
    //
    int16_t index = -32768;
    while (1) {
        uint16_t bits = index;
        uint16_t blueBits = bits & blueMask;
        p->blue = blueValues.at(blueBits);
        bits = bits >> blueBitCount;
        uint16_t greenBits = bits & greenMask;
        p->green = greenValues.at(greenBits);
        bits = bits >> greenBitCount;
        uint16_t redBits = bits & redMask;
        p->red = redValues.at(redBits);
        LOGDEBUG << index << ' ' << std::hex << redBits << ' ' << greenBits << ' ' << blueBits << ' ' << std::dec
                 << p->red << ' ' << p->green << ' ' << p->blue << std::endl;
        if (index == 32767) break;
        ++index;
        ++p;
    }
}

